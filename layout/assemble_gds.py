# Multi-rail bridge helper
# ═══════════════════════════════════════════════════

def _draw_rail_bridges(cell, li_m2, li_v2, li_m3, rails, m2_signal_segs):
    """Connect same-net M3 rails with M2 underpass bridges.

    For each pair of same-net rails, draws:
    - Via2 on each rail at a shared X position
    - M2 vertical bar between the two Via2 points

    X position chosen at the left margin to avoid M2 signal conflicts.
    """
    from collections import defaultdict

    # Group rails by net
    net_rails = defaultdict(list)
    for rail_id, rail in rails.items():
        net = rail.get('net', rail_id)
        net_rails[net].append((rail_id, rail))

    bridge_count = 0
    # Assign distinct X positions per net to avoid M2 collision between bridges
    net_idx = 0
    for net, rail_list in sorted(net_rails.items()):
        if len(rail_list) < 2:
            continue

        # Sort by Y
        rail_list.sort(key=lambda r: r[1]['y'])

        # Bridge each adjacent pair
        for i in range(len(rail_list) - 1):
            rid1, r1 = rail_list[i]
            rid2, r2 = rail_list[i + 1]

            y1 = r1['y']
            y2 = r2['y']

            # Choose X: offset each net by 2µm to avoid inter-bridge M2 shorts
            # First net at left margin, next at left+2µm, etc.
            base_x = r1['x1'] + 1500
            bridge_x = base_x + net_idx * 2000

            # Check for M2 signal conflicts at this X column
            hp = VIA2_PAD // 2
            has_conflict = False
            for sx1, sy1, sx2, sy2 in m2_signal_segs:
                sw = M2_SIG_W // 2
                # Check if M2 signal overlaps our bridge column
                seg_x1 = min(sx1, sx2) - sw
                seg_x2 = max(sx1, sx2) + sw
                seg_y1 = min(sy1, sy2)
                seg_y2 = max(sy1, sy2)
                if (bridge_x - hp < seg_x2 and bridge_x + hp > seg_x1 and
                        seg_y1 < y2 and seg_y2 > y1):
                    has_conflict = True
                    break

            if has_conflict:
                # Shift to right margin
                bridge_x = r1['x2'] - 1500 - net_idx * 2000

            # Draw Via2 at both rails
            via2(cell, li_v2, li_m2, li_m3, bridge_x, y1)
            via2(cell, li_v2, li_m2, li_m3, bridge_x, y2)

            # Draw M2 vbar between them (narrowed to M2_MIN_W for spacing)
            vbar(cell, li_m2, bridge_x, y1, y2, M2_MIN_W)

            bridge_count += 1
            print(f'    Bridge {rid1} <-> {rid2} ({net}): '
                  f'x={bridge_x / 1000:.1f}µm, '
                  f'y={y1 / 1000:.1f}-{y2 / 1000:.1f}µm')

        net_idx += 1

    print(f'  Drew {bridge_count} multi-rail bridges')


