def main():
    placement, ties, routing = load_jsons()

    lib = pya.Library.library_by_name(LIB_NAME)
    if not lib:
        raise RuntimeError(f'Library {LIB_NAME} not found')

    layout = klayout.db.Layout(True)
    layout.dbu = 0.001  # nm
    top = layout.cell(layout.add_cell('soilz'))

    # ── Layer indices ──
    li_m1 = layout.layer(*METAL1)
    li_m2 = layout.layer(*METAL2)
    li_m3 = layout.layer(*METAL3)
    li_m4 = layout.layer(*METAL4)
    li_m5 = layout.layer(*METAL5)
    li_v1 = layout.layer(*VIA1)
    li_v2 = layout.layer(*VIA2)
    li_v3 = layout.layer(*VIA3)
    li_v4 = layout.layer(*VIA4)
    li_tv1 = layout.layer(*TOPVIA1)
    li_tm1 = layout.layer(*TOPMETAL1)

    # Router layer mapping (router code → physical GDS layer):
    #   Router layer 0 → Metal3, 1 → Metal4, 2 → Metal5
    #   Router via -1  → Via3,   -2 → Via4
    # draw_segments receives li_m1/m2/m3/m4 positionally, so we remap:
    li_route_0 = li_m3   # router layer 0 = M3
    li_route_1 = li_m4   # router layer 1 = M4
    li_route_2 = li_m5   # router layer 2 = M5
    li_via_01  = li_v3   # router via -1 = Via3 (M3↔M4)
    li_via_12  = li_v4   # router via -2 = Via4 (M4↔M5)
    li_m3_lbl = layout.layer(30, 25)   # LVS: metal3_text = labels(30, 25)
    li_m2_lbl = layout.layer(10, 25)   # LVS: metal2_text = labels(10, 25)

    # ── Schema validation ──
    from assemble.schema_check import print_validation
    print_validation(placement, ties, routing)

    # ═══ 1. Place PCells ═══
    from assemble.context import AssemblyContext
    from assemble.pcell_place import place_pcells
    _ctx = AssemblyContext(layout, top, placement, ties, routing,
                           None, _device_lib, DEVICES)
    if place_pcells(_ctx, lib):
        return  # BARE_MODE
    instances = placement['instances']

    # Collect Via1 centers from ALL drawing phases for signal-route dedup.
    # Prevents duplicate Via1 at positions already drawn by gate contacts,
    # access points, or power drops.
    drawn_vias = set()

    # ═══ 1a. M1 S/D bus straps ═══
    from assemble.bus_straps import draw_bus_straps
    _netlist_bus = json.load(open(os.path.join(os.path.dirname(os.path.abspath(__file__)), 'netlist.json')))
    _pin_net_bus = {}
    for _ne in _netlist_bus.get('nets', []):
        for _pin in _ne['pins']:
            _pin_net_bus[_pin] = _ne['name']
    (bus_count, bus_tie_adj, bus_gap_cuts, bus_m2_bridges,
     _bus_m3_bridges) = draw_bus_straps(
        top, li_m1, li_m2, li_m3, li_m4, li_v1, li_v2, li_v3,
        instances, placement, ties, routing,
        _device_lib, DEVICES, _pin_net_bus, drawn_vias)
    print(f'  Drew {bus_count} M1 S/D bus straps'
          f'{f" ({bus_tie_adj} adjusted for tie M1)" if bus_tie_adj else ""}'
          f'{f" ({bus_gap_cuts} cross-net gaps cut)" if bus_gap_cuts else ""}'
          f'{f" ({bus_m2_bridges} M2 S0 bridges)" if bus_m2_bridges else ""}')
    _ap_m1_obs = []
    for _key, _ap in routing.get('access_points', {}).items():
        _net = _pin_net_bus.get(_key, '')
        if not _net: continue
        _stub = _ap.get('m1_stub')
        if _stub: _ap_m1_obs.append((_stub[0], _stub[1], _stub[2], _stub[3], _net))
        _vp = _ap.get('via_pad', {})
        if 'm1' in _vp:
            _r = _vp['m1']
            _ap_m1_obs.append((_r[0], _r[1], _r[2], _r[3], _net))

    # Inline DRC tracker (incremental)
    from assemble.drc_check import DRCTracker, check_layer, check_metal_layers
    _drc = DRCTracker(top, {
        'M1': (li_m1, M1_MIN_S, M1_MIN_W),
        'M2': (li_m2, M2_MIN_S, M2_MIN_W),
        'M3': (li_m3, M3_MIN_S, M3_MIN_W),
    })
    _drc.checkpoint('bus_straps')

    # ═══ 1b+1c. Gate straps + contacts ═══
    from assemble.gate import draw_gate_straps_and_contacts
    (strap_count, gate_cont_count, gate_bridge_count,
     gate_cont_m1) = draw_gate_straps_and_contacts(
        top, li_m1, layout, instances,
        _device_lib, DEVICES, _gate_info)
    print(f'  Drew {strap_count} gate straps')
    print(f'  Drew {gate_cont_count} gate contacts'
          f'{f", {gate_bridge_count} poly bridges (ng>=4)" if gate_bridge_count else ""}')

    _drc.checkpoint('gate')

    # ═══ 2. Tie cells + NWell ═══
    from assemble.ties_nwell import draw_ties_and_nwell
    li_nw = layout.layer(*NWELL)
    li_psd = layout.layer(*PSD)
    draw_ties_and_nwell(top, li_m1, li_nw, li_psd, layout, instances,
                         placement, ties, routing, routing.get('access_points', {}),
                         _device_lib, DEVICES, _pin_net_bus,
                         _ap_m1_obs, gate_cont_m1)

    _drc.checkpoint('ties_nwell')

    # ═══ 3. Access points ═══
    from assemble.access_points import draw_access_points
    _ap_count, _m2_route_wires = draw_access_points(
        top, li_m1, li_m2, li_v1, layout,
        instances, routing, _device_lib, DEVICES,
        _pin_net_bus, drawn_vias, _ap_m1_obs)

    _drc.checkpoint('access_points')

    # ═══ 4. Power ═══
    from assemble.power import draw_power
    from atk.route.maze_router import M2_LYR
    _drop_count, _gap_bridge_m3_pads, _gap_bridge_m3_jogs, _m2_signal_segs = draw_power(
        top, li_m1, li_m2, li_m3, li_m4, li_m5,
        li_v1, li_v2, li_v3, li_v4, li_tv1, li_tm1,
        layout, instances, placement, routing,
        _device_lib, DEVICES, _pin_net_bus,
        drawn_vias, _bus_m3_bridges, _m2_route_wires)

    _drc.checkpoint('power')

    # ── Pre-scan: detect floating routes (no M5 endpoint near any AP) ──
    from atk.route.maze_router import M3_LYR, M4_LYR
    _VIA2_REACH = 500
    _floating_count = 0
    _via_stack_pins = set()
    for _d in routing.get('power', {}).get('drops', []):
        if _d['type'] == 'via_stack':
            _via_stack_pins.add(f"{_d['inst']}.{_d['pin']}")
    _shared_pins = set()
    _pos_map = {}
    for _ak, _av in routing.get('access_points', {}).items():
        _pos_map.setdefault((_av['x'], _av['y']), []).append(_ak)
    for _pos, _keys in _pos_map.items():
        if len(_keys) >= 2:
            _shared_pins.update(_keys)
    for _fn, _fr in routing.get('signal_routes', {}).items():
        _fsegs = _fr.get('segments', [])
        if not _fsegs:
            _fr['_floating'] = True
            _floating_count += 1
            continue
        _has_candidate = False
        for _fp in _fr.get('pins', []):
            if _fp in _via_stack_pins or _fp in _shared_pins:
                continue
            _fap = routing.get('access_points', {}).get(_fp)
            if not _fap or not _fap.get('via_pad') or 'm2' not in _fap.get('via_pad', {}):
                continue
            _fax, _fay = _fap['x'], _fap['y']
            for _fs in _fsegs:
                if len(_fs) < 5 or _fs[4] not in (M3_LYR, M4_LYR, -3):
                    continue
                for _sx, _sy in ((_fs[0], _fs[1]), (_fs[2], _fs[3])):
                    if abs(_sx - _fax) + abs(_sy - _fay) <= _VIA2_REACH:
                        _has_candidate = True
                        break
                if _has_candidate:
                    break
            if _has_candidate:
                break
        if not _has_candidate:
            _fr['_floating'] = True
            _floating_count += 1
    if _floating_count:
        print(f'  Pre-scan: {_floating_count} floating routes detected')

    # ═══ 5. Signal routing ═══
    from assemble.signal_routes import draw_signal_routes
    _fallback_shapes = draw_signal_routes(top, li_m1, li_m2, li_m3, li_m4, li_m5,
                       li_v1, li_v2, li_v3, li_v4,
                       li_route_0, li_route_1, li_route_2,
                       li_via_01, li_via_12,
                       routing, instances, placement,
                       _device_lib, DEVICES, _pin_net_bus,
                       drawn_vias, _bus_m3_bridges, gate_cont_m1, _m2_route_wires, _gap_bridge_m3_pads, _gap_bridge_m3_jogs)

    _drc.checkpoint('signal_routes')
    _drc.report()

    # ═══ 6. Pin labels ═══
    from assemble.labels import draw_labels
    draw_labels(top, layout, li_m2_lbl, li_m3_lbl, routing, _pin_net_bus)

    # ── Inline LVS proxy ──
    from assemble.lvs_check import check_power_connectivity
    check_power_connectivity(layout, top, ties)

    # ═══ Summary ═══
    bb = top.bbox()
    w_um = bb.width() * 0.001
    h_um = bb.height() * 0.001
    area_um2 = w_um * h_um
    print(f'\n  Bounding box: {w_um:.1f} x {h_um:.1f} µm = {area_um2:.0f} µm²')
    print(f'  Tile 1×2: 202.1 × 313.7 µm = 63,399 µm²')
    print(f'  Utilization: {area_um2/63399*100:.1f}%')

    # ═══ Write GDS ═══
    default_gds = os.path.join(os.path.dirname(os.path.abspath(__file__)), 'output', 'soilz.gds')
    output = os.environ.get('GDS_OUTPUT', default_gds)
    layout.write(output)
    print(f'\n  Written to {output}')

    # ── Post-write fixup: re-add fallback shapes lost by PCell ──
    # KLayout PCell materialization during write can lose shapes added
    # directly to the top cell. Re-read the flat GDS and add them.
    print(f'  DEBUG: _fallback_shapes has {len(_fallback_shapes)} entries')
    if _fallback_shapes:
        _LAYER_MAP = {'Via2': VIA2, 'Via3': VIA3, 'M2': METAL2,
                      'M3': METAL3, 'M4': METAL4}
        _layout2 = klayout.db.Layout()
        _layout2.read(output)
        _top2 = None
        for _ci in range(_layout2.cells()):
            if _layout2.cell(_ci).name == 'soilz':
                _top2 = _layout2.cell(_ci)
                break
        if _top2:
            _n_added = 0
            for _lname, _x1, _y1, _x2, _y2 in _fallback_shapes:
                _li = _layout2.layer(*_LAYER_MAP[_lname])
                # Check if shape already exists (skip duplicates)
                _cx = (_x1 + _x2) // 2
                _cy = (_y1 + _y2) // 2
                _exists = False
                for _sh in _top2.shapes(_li).each():
                    _bb = _sh.bbox()
                    _scx = (_bb.left + _bb.right) // 2
                    _scy = (_bb.top + _bb.bottom) // 2
                    if abs(_scx - _cx) < 10 and abs(_scy - _cy) < 10:
                        _exists = True
                        break
                if not _exists:
                    _top2.shapes(_li).insert(
                        klayout.db.Box(_x1, _y1, _x2, _y2))
                    _n_added += 1
            print(f'  Post-write fixup: {_n_added} missing / {len(_fallback_shapes)}'
                  f' total fallback shapes')
            if _n_added:
                _layout2.write(output)


if __name__ == '__main__':
    main()
