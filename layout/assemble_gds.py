def _parse_tie_layer(key):
    """Parse 'Activ_1_0' → (1, 0)."""
    parts = key.rsplit('_', 2)
    if len(parts) == 3:
        return (int(parts[1]), int(parts[2]))
    # Fallback: try name lookup
    name = parts[0]
    return _TIE_LAYER_MAP.get(name, None)


