def _add_tff_nwell_bridges(cell, layout, placement):
    """Add NWell bridges to merge PMOS NWells in each TFF half-stage.

    Each TFF half (e.g. T1I master) has 4 PMOS devices (m2/m4/m5/m6) with
    isolated NWell islands.  Only m4/s4 has an ntap NWell extension.
    Without a bridge, m2/m5/m6/s2/s5/s6 fail LU.a (PMOS >20µm from ntap).

    Fix: draw a horizontal NWell strip spanning all PMOS in each half,
    merging their NWells so the single ntap covers all devices.
    """
    import re

    li_nw = layout.layer(*NWELL)
    instances = placement['instances']

    # Group TFF PMOS by (stage, half, y)
    groups = {}
    for name, info in instances.items():
        if info['type'] != 'pmos_vco' or not name.startswith('T'):
            continue
        m = re.match(r'(T\w+)_([ms])\d', name)
        if not m:
            continue
        key = (m.group(1), m.group(2), info['y_um'])
        groups.setdefault(key, []).append(info)

    # PCell NWell: 1800nm wide, 2620nm tall, origin at (x_um*UM, y_um*UM)
    NW_H = 2620  # pmos_vco NWell height (nm)
    NW_W = 1800  # pmos_vco NWell width (nm)
    NW_A_MIN = 620  # NW.a min NWell width

    count = 0
    for (stage, half, y_um), devs in sorted(groups.items()):
        if len(devs) < 2:
            continue

        xs = sorted(info['x_um'] for info in devs)
        x1 = int(xs[0] * UM)                   # leftmost device NWell left
        x2 = int(xs[-1] * UM) + NW_W           # rightmost device NWell right
        y_top = int(y_um * UM) + NW_H           # device NWell top
        y_bot = y_top - NW_A_MIN                # bridge bottom (620nm strip)

        cell.shapes(li_nw).insert(klayout.db.Box(x1, y_bot, x2, y_top))
        count += 1

    if count:
        print(f'  Drew {count} TFF NWell bridges (LU.a fix)')


def _add_nwell_bridge(cell, layout, placement):
    """Add NWell bridge between MBp1 and MBp2 to avoid NW.b1 PWell width violation.

    Reads actual PCell NWell shapes from the layout to determine precise Y range,
    then draws a bridging rectangle at the Y intersection.
    """
    li_nw = layout.layer(*NWELL)

    # MBp1 and MBp2 placement bounding boxes (nm)
    mbp1 = placement['instances'].get('MBp1')
    mbp2 = placement['instances'].get('MBp2')
    if not mbp1 or not mbp2:
        return

    mbp1_x1 = s5(mbp1['x_um'])
    mbp1_x2 = mbp1_x1 + s5(mbp1['w_um'])
    mbp2_x1 = s5(mbp2['x_um'])
    mbp2_x2 = mbp2_x1 + s5(mbp2['w_um'])

    # Scan NWell shapes in the layout to find actual PCell NWell Y extents
    # for each device region
    mbp1_nw_ymin, mbp1_nw_ymax = None, None
    mbp1_nw_xmax = None
    mbp2_nw_ymin, mbp2_nw_ymax = None, None
    mbp2_nw_xmin = None

    for si in cell.begin_shapes_rec(li_nw):
        box = si.shape().bbox().transformed(si.trans())
        cx = (box.left + box.right) // 2
        # Attribute NWell shape to MBp1 or MBp2 based on X center
        if mbp1_x1 - 500 <= cx <= mbp1_x2 + 500:
            if mbp1_nw_ymin is None or box.bottom < mbp1_nw_ymin:
                mbp1_nw_ymin = box.bottom
            if mbp1_nw_ymax is None or box.top > mbp1_nw_ymax:
                mbp1_nw_ymax = box.top
            if mbp1_nw_xmax is None or box.right > mbp1_nw_xmax:
                mbp1_nw_xmax = box.right
        if mbp2_x1 - 500 <= cx <= mbp2_x2 + 500:
            if mbp2_nw_ymin is None or box.bottom < mbp2_nw_ymin:
                mbp2_nw_ymin = box.bottom
            if mbp2_nw_ymax is None or box.top > mbp2_nw_ymax:
                mbp2_nw_ymax = box.top
            if mbp2_nw_xmin is None or box.left < mbp2_nw_xmin:
                mbp2_nw_xmin = box.left

    if any(v is None for v in [mbp1_nw_ymin, mbp1_nw_ymax, mbp1_nw_xmax,
                                mbp2_nw_ymin, mbp2_nw_ymax, mbp2_nw_xmin]):
        print('    WARNING: could not determine PCell NWell extents for bridge')
        return

    # Y intersection (safe bridge range)
    bridge_ymin = max(mbp1_nw_ymin, mbp2_nw_ymin)
    bridge_ymax = min(mbp1_nw_ymax, mbp2_nw_ymax)
    if bridge_ymin >= bridge_ymax:
        print('    WARNING: NWell Y ranges do not overlap, cannot bridge')
        return

    # X range: from MBp1 NWell right to MBp2 NWell left
    bridge_x1 = mbp1_nw_xmax
    bridge_x2 = mbp2_nw_xmin

    if bridge_x1 >= bridge_x2:
        print(f'    NWell already overlaps (x1={bridge_x1} >= x2={bridge_x2}), no bridge needed')
        return

    cell.shapes(li_nw).insert(klayout.db.Box(bridge_x1, bridge_ymin, bridge_x2, bridge_ymax))
    print(f'    NWell bridge: [{bridge_x1}, {bridge_ymin}, {bridge_x2}, {bridge_ymax}] '
          f'({(bridge_x2-bridge_x1)/1000:.1f} x {(bridge_ymax-bridge_ymin)/1000:.1f} µm)')


def _fill_nwell_gaps(cell, layout):
    """Fill NWell notches using morphological close.

    Closes same-net NWell notches (NW.b < 620nm) by applying
    sized(+r) → merge → sized(-r).  Does NOT attempt to fix NW.b1
    (different-net spacing) — those gaps contain NMOS active areas.
    """
    NW_B_MAX = 620  # NW.b threshold (same-net notch)

    li_nw = layout.layer(*NWELL)
    original = klayout.db.Region(cell.begin_shapes_rec(li_nw)).merged()
    if original.is_empty():
        return

    r = NW_B_MAX // 2  # 310nm
    closed = original.sized(r).merged().sized(-r)
    fills = closed - original

    count = 0
    for poly in fills.each():
        cell.shapes(li_nw).insert(poly)
        count += 1

    if count:
        print(f'    NWell notch close: {count} fill regions')


